# Operadores aritmeticos
num1 = 77
num2 = 44

suma = num1 + num2
resta = num1 - num2
multiplicacion = num1 * num2
division = num1 / num2
resto = num1 % num2

print("*******************CALCULADORA*******************")
print(f"Suma: {suma}")
print(f"La resta es: {resta}")
print(f"La multiplicacion es: {multiplicacion}")
print(f"La division es: {division}")
print(f"El resto es: {resto}")

#Operadores de asignacion 
edad = 55
edad += 5 # Suma 5 al valor actual de la variable 
edad = edad + 5
edad -= 5 # Resta 5 al valor actual de la variable

